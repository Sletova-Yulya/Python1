def exponent_func_new(x, y):
    """
    Функуия возводит число в степень (как в положительную, так и в отрицательную) с использованием цикла while.
    :param x: float, действительное положительное число
    :param y: int, целое число
    :return: float, результат возведения в степень
    """
    count = 2
    exp_x = x
    while count <= abs(y):
        exp_x = exp_x * x
        count += 1
    if y > 0:
        exp_x_total = exp_x
    elif y == 0:
        exp_x_total = 1
    else:
        exp_x_total = 1 / exp_x
    return(exp_x_total)

print(exponent_func_new(5, -3))

