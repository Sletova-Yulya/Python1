# Не очень я поняла, чем ** отличаются по сложности от использования встроенной функции pow()

def exponent_func(x, y):
    """
    Функция возводит в степень.
    :param x: float, действительное положительное число
    :param y: int, целое отрицательное число
    :return: float, результат возведения в степень
    """
    exp = x ** y
    # exp = 1 / (x ** abs(y)) если предположить, что Python не умеет возводить в отрицательную степень.
    return exp

a = exponent_func(5, -5)
print(a)

